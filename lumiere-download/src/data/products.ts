/** Returns all categories including nested subcategories as a flat list */
export const getAllCategories = (): Category[] => {
  const flat: Category[] = [];
  categories.forEach((cat) => {
    flat.push(cat);
    if (cat.subcategories) {
      flat.push(...cat.subcategories);
    }
  });
  return flat;
};

/** Returns all diamond subcategory slugs */
export const getDiamondSubcategorySlugs = (): string[] => {
  const diamondCat = categories.find((c) => c.slug === 'diamonds');
  return diamondCat?.subcategories?.map((s) => s.slug) ?? [];
};

